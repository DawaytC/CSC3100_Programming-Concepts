import java.util.Scanner;
public class Six_problem4 {
	public static void main(String args[]) {

		//number of rows
		int rows = 6;
	    //first loop
		for (int i = 1; i <= rows; ++i) {
	    //secondloop
			for (int k = 1; k <= i; ++k) {
	        System.out.print(k + " ");
	      }
	      System.out.println("");
	    }
	}	
}




